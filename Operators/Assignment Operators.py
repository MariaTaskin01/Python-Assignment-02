print("1. Start with a variable `x = 0` and apply `+=`, `-=`, `*=`, `/=`, `%=`, step by step.")

x = 0
print("Initial value of x:", x)

x += 5
print("x += 5:", x)

x -= 3
print("x -= 3:", x)

x *= 2
print("x *= 2:", x)

x /= 4
print("x /= 4:", x)

x %= 3
print("x %= 3:", x)

